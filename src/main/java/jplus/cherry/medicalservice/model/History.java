package jplus.cherry.medicalservice.model;

import java.util.Date;

public class History {
    private Integer id;

    private Integer userid;

    private String diagnosiscontent;
    
    private String prescription;
    
    private Date diagnosistime;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getUserid() {
		return userid;
	}

	public void setUserid(Integer userid) {
		this.userid = userid;
	}

	public String getDiagnosiscontent() {
		return diagnosiscontent;
	}

	public void setDiagnosiscontent(String diagnosiscontent) {
		this.diagnosiscontent = diagnosiscontent;
	}

	public String getPrescription() {   
		return prescription;
	}

	public void setPrescription(String prescription) {
		this.prescription = prescription;
	}
	
	public Date getDiagnosistime() {
		return diagnosistime;
	}

	public void setDiagnosistime(Date diagnosistime) {
		this.diagnosistime = diagnosistime;
	}

	@Override
	public String toString() {
		return "History [id=" + id + ", userid=" + userid + ", diagnosiscontent=" + diagnosiscontent + ", prescription="
				+ prescription + "]";
	}

}